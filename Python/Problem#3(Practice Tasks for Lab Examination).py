day = int(input("Enter a number from 1 to 7: "))

if day == 1:
    print(f"Saturday")
elif day == 2:
    print(f"Sunday")
elif day == 3:
    print(f"Monday")
elif day == 4:
    print(f"Tuesday")
elif day == 5:
    print(f"Wednesday")
elif day == 6:
    print(f"Thursday")
elif day == 7:
   print(f"Friday")
else:
    print("Wrong input! Please enter a number from 1 to 7.")
